    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        if (this.verifCoordonnees() == -1) {
            return;
        }
        String IdEIn = IdE.getText().trim();
        int IdCIn = Integer.valueOf(IdC.getText());
        boolean justifie = Justification.getText().trim() == "";
        String justification = Justification.getText().trim();
        Ab.ajouter(IdEIn, IdCIn, justifie, justification);
        this.dispose();
    }//GEN-LAST:event_AddBtnActionPerformed

    private void IdCKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_IdCKeyTyped
        char c = evt.getKeyChar();
        if (!Character.isDigit(c)) {
            evt.consume();
        }
    }//GEN-LAST:event_IdCKeyTyped
    public int verifCoordonnees() {
        String IdEIn = IdE.getText().trim();
        int IdCIn = Integer.valueOf(IdC.getText());
        if (IdEIn == "") {
            displayError("Vous devez taper l'identifiant de l'etudiant");
            return -1;
        }
        if (IdCIn<=0) {
            displayError("Vous devez taper l'identifiant du cours");
            return -1;
        }
 
        return 0;
    }

    public void displayError(String reason) {
        JFrame frame = new JFrame("Error Dialog");
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);

        JOptionPane.showMessageDialog(frame, reason, "Erreur", JOptionPane.ERROR_MESSAGE);

        frame.dispose();
    }
