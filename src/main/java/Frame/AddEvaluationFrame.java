    private void SaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBtnActionPerformed
        if (this.verifCoordonnees() == -1) {
            return;
        }
        String IdEIn = IdE.getText().trim();
        int IdMIn = Integer.valueOf(IdM.getText());
        String type = TypeNote.getSelectedItem().toString();
        float note = Float.valueOf(Note.getText());
        Ev.ajouter(IdEIn, IdMIn, note, type);
        this.dispose();
    }//GEN-LAST:event_SaveBtnActionPerformed

